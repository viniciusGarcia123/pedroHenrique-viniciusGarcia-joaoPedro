                <h1>Review Clássicos: Mega Man 2</h1>
                <p><b>JOGO:</b> Mega Man 2 <br><br>
                    <b>ANO DE LANÇAMENTO:</b> 1988 <br><br>
                    <b>PLATAFORMAS: NES (Eua)/ Famicon (Japão)</b> <br><br>
                    <img src="imagem1.png" class="MM2" alt="robo">
                    <br><br>
                    <b>ANÁLISE:</b> No ano de 200X, o robô Rock foi criado por Dr. Light. A principio era apenas um robô doméstico, sendo tratado como um filho por seu criador, mas devido o caos causado pelo vilão Dr. Wily, Rock foi modificado para combater e mal e se tornou Mega Man. <br><br>
O jogo é sequência de Mega Man 1 (1987, NES) e segue basicamente o mesmo conceito. Ao iniciar o jogo você escolhe a fase de acordo com seu respectivo chefe e seu objetivo é derrota-lo e obter seu poder, prosseguindo assim até derrotar os 8 chefes e liberar as fases finais do vilão, Dr. Wily. <br><br><br>
                    <img src="imagem2.png" class="MM2" alt="lalala">
                      <br><br><br>
Mega Man 2 se tornou um clássico por ser desafiador e devido a sua simplicidade de design, funcionando perfeitamente de acordo com as limitações do NES. O gráfico foi considerado avançado para a época, sprites e fundos simples, porém coloridos e entendíveis. A trilha sonora do jogo é fantástica, temas “pegajosos” e muito bem trabalhados com uma pegada de Rock ‘n Roll perfeitamente adaptadas para a placa de áudio do console. Os controles seguem perfeitamente as limitações do NES, baseando-se apenas em andar, pular e atirar as diversas armas selecionáveis através do pause, sendo possível controlar perfeitamente a altura do pulo, pular e atirar ao mesmo tempo e, dependendo da arma, a direção do tiro. <br><br><br>
                    <img src="imagem%203.png" class="MM2" alt="mais robo">
                    <br><br><br>
Considerado pelas grandes mídias como um dos maiores clássicos de todos os tempos, Mega Man 2 prevalece o passar dos anos, tendo “port” praticamente em todas gerações de consoles, assim desafia as habilidades dos gamers de todas as idades.
