            <h1> The Witcher 3: Wild Hunt - Entrevista com Damien Monnier - Tradução exclusiva da Witcher Br</h1>
            <img class="im" src="dd.jpg" alt="tiozinho">
<p>Um jogo com uma escala tão grande como o The Witcher 3, acaba trazendo algumas dúvidas para os fãs. Para esclarecer elas, foi feita, por intermédio do usuário e moderador Kinley, do Forum do jogo, uma série de perguntas e respostas com o Dev Damien Monnier, Designer de Games da CD Projekt RED. A Witcher BR é a tradutora oficial desse série para o português.
    São quase 36 páginas com perguntas que variam de combate à questão do romance do personagem. É uma ótima leitura que certamente irá responder suas dúvidas e despertar ainda mais seu interesse pelo jogo! </p>
<h3>Como você se tornou um designer de gameplay? Sempre foi seu plano tornar-se um?</h3>
<p>Não, eu queria ser um cowboy aos 6, depois um designer de joias na Escola de Artes. Mas olhando para isso eu costumava fazer games de tabuleiro rústicos/ e do tipo dungeon crawler quando eu tinha mais ou menos 9 anos para meus amigos jogarem e nunca vencerem, porque o balanceamento não existia, então eu acho que deveria ter percebido que meu lance era fazer jogos, e ter parado de fazer joias mais cedo. Sem arrependimentos.</p>

<h3>Quais são alguns dos projetos que você trabalhou no passado?</h3>
<p>O projeto mais conhecido provavelmente seria a série Motorstorm que foram games off road realmente divertidos, bem como alguns games incríveis que foram cancelados, os quais eu não posso falar a respeito, e alguns jogos portáteis naqueles tempos quando o velho Nokia e o jogo da cobrinha eram coisa quente. :) E só deus sabe quantos projetos pessoais estão em andamento. Meu currículo é uma miscelânea que inclui jogos de quebra-cabeça, de corrida e jogos de ação.</p>
<h3>O que o papel de designer de gameplay implica? O que você faz exatamente?</h3>
<p>Eu trabalho com pequenos times, os quais incluem programadores, artistas, para criar elementos para o jogo. O gameplay não necessariamente implica em coisas interativas mas ele pode abranger desde o design de armas, monstros, implementação de características que são mais do que um design de game por trás da cortina ou até um design puro como nosso mninigame Gwent que é basicamente um jogo de cartas.</p>
<h3>Como um designer de gameplay interage com membros de outros times no estúdio?</h3>
<p>Sendo legal e dizendo "confie em mim, eu vou trabalhar muito".</p>
<h3>Você poderia nos falar brevemente sobre como é um típico dia no escritório como um designer de gameplay?</h3>
<p>Eu tento chegar cedo, pego um café e olho a situação de vários projetos que estou trabalhando no dia e priorizo meu dia. Em alguns casos eu consigo manter minha cabeça abaixada e gastar um dia inteiro fazendo trabalho de implementação (digamos, popular uma área inteira com coisas vivas) mas é mais frequente que eu tenha algumas atividades para fazer ao mesmo tempo. A maioria dos designers são assim, você meio que toma posse de algum elemento e você torna-se a pessoa para aquele elemento até o final do jogo.</p>

<br><img class="im" src="ddd.jpg" alt="jogo">
<h3>Você pode citar algumas ferramentas que você usa diariamente como designer de gameplay?</h3>

<p>Cada estúdio tem suas próprias ferramentas, a não ser que eles usem Unreal ou Unity (apenas como exemplo) o que acontenceu em um dos estúdios que trabalhei, então no meu caso tive que aprender novas ferramentas todas as vezes que troquei de estúdio. Eu uso nossa Red Engine 3 todo dia é claro, mas também utilizo nosso editor de script o qual é chamado... Editor de Script ou Sublime para XML (que são utilizados para guardar a definição de nossos itens entre outras coisas.</p>

<h3>Qual sua maior inspiração quando se trata de design de game?</h3>

<p>Jogos de tabuleiro são realmente bons para mecânica pura, eu tento jogar bastante jogos de tabuleiro jogos de cartas, ao menos uma vez, para que eu possa entender as mecânicas.</p>


<p>No mundo do The Witcher, quem os level designers seriam? Que tipo de pensamento eles adotariam? Eles seriam camponeses humildes? Ou talvez um poderoso governante?</p>

<p>Um bêbado, o qual é também realmente amargo.</p>


<h3>Você tem algum concelho para alguém que entrar no design de gameplay?
Considerando que eles tenham experiência zero e não sabem onde começar.</h3>

<p>Façam alguma coisa. Peguem alguma ferramenta como GameMaker. Assistam alguns tutoriais e façam algo pequeno. Mesmo que seja feio não importa, a chave é terminar e começar outro. Jogar um monte de videogames é legal para pesquisa (e para diversão!), mas justamente como assistir um monte de filmes não te fazem um diretor, jogar videogames não te fazem um designer. Teoria dos Jogos é muito importante também, você precisa realmente entender os humanos e o que nos agrada, o que nos mantem pressionando esses botões, os macacos que somos. :)</p>



